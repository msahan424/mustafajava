package day08_IfStatement;

public class SwapTwoVariables {
    public static void main(String[] args) {
        int a= 10;
        int b =15;
        System.out.println("a = " + a);
        System.out.println("b = " + b);
        //----------------------------------------------
        b=a; //b=10
        a=b;//a= 10
        //-----------------------------------------------------
        System.out.println("a = " + a);
        System.out.println("b = " + b);
    }
}
