package day15_FoorLoop;

public class ForLoopExamples {
    public static void main(String[] args) {
        //30,32,34...90 and "90 years" to println
        for (int i=15; i<=45; i++){
           int k =2*i;
            System.out.println(k);

        }
        System.out.println("90 Years");
    }
}
